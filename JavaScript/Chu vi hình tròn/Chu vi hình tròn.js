 function TinhChuvi(){
    let r =document.getElementById('r').value;
    let chuvi = 2 * r * Math.PI;
    alert ('Chu Vi Hinh Tron = :' + chuvi);
 }
 function TinhDienTich() {
    let r = document.getElementById('r').value;
    let dientich = r * r * Math.PI;
    alert('Dien Tich Hinh Tron =' + dientich);
 }